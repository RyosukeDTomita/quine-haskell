main :: IO ()
main = putStr $ toQ (cycle (s ++ show s)) (unlines h)
  where
    toQ (c : cs) (x : xs)
      | c /= ' ' && x == '*' = c : toQ cs xs
      | c == ' ' = toQ cs (x : xs)
      | c == '\n' = '\\' : 'n' : toQ cs (x : xs)
      | otherwise = x : toQ (c : cs) xs
    toQ _ _ = []

